<h1>PDO</h1>
  <img src="images/pdo.png">
  <div id="inhoud"><p>PDO is een relationele database-adapter voor PHP om een interface te bieden naar MySQL-databases. Het vervangt de verouderde MySQL-adapter die vanaf PHP6 niet meer ondersteund wordt. Vanaf MySQL Server 4.1.3 wordt aangeraden om PDO of MySQLi te gebruiken in plaats van de verouderde MySQL-adapter.</p></div>
